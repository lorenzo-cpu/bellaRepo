package com.mycompany.rettangolo;

public class Punto {
    
    private double ascissa, ordianata;
    
    public Punto(double x, double y)
    {
        this.ascissa = x;
        this.ordianata = y;
    }
    
    public double getAscissa()
    {
        return ascissa;
    }
    
    public double getOrdinata()
    {
        return ordianata;
    }
}
